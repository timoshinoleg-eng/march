export type PromptVariant = 'consultant' | 'technical' | 'friendly' | 'faq' | 'sales' | 'support' | 'brief';
export type WidgetMode = 'faq' | 'sales' | 'support';

export interface PromptSelectionContext {
  widgetMode?: WidgetMode;
  detectedTone?: 'formal' | 'casual' | 'technical';
  leadStage?: 'awareness' | 'evaluation' | 'decision' | 'purchase';
  isTechnicalQuestion?: boolean;
  messageCount?: number;
  isBriefCollection?: boolean;
}

/**
 * Select appropriate prompt based on context
 */
export function selectPrompt(context: PromptSelectionContext): string {
  // Brief collection mode (highest priority)
  if (context.isBriefCollection) {
    return BRIEF_COLLECTION_PROMPT;
  }

  // Mode-based selection
  if (context.widgetMode) {
    switch (context.widgetMode) {
      case 'faq':
        return FAQ_PROMPT;
      case 'sales':
        return SALES_PROMPT;
      case 'support':
        return SUPPORT_PROMPT;
    }
  }

  // Tone-based selection
  if (context.detectedTone === 'technical' || context.isTechnicalQuestion) {
    return TECHNICAL_PROMPT;
  }

  if (context.detectedTone === 'casual') {
    return FRIENDLY_PROMPT;
  }

  // Lead stage-based selection
  if (context.leadStage === 'evaluation' || context.leadStage === 'decision') {
    return CONSULTANT_PROMPT;
  }

  // Default
  return CONSULTANT_PROMPT;
}

/**
 * Get prompt by variant name
 */
export function getPromptByVariant(variant: PromptVariant): string {
  switch (variant) {
    case 'consultant':
      return CONSULTANT_PROMPT;
    case 'technical':
      return TECHNICAL_PROMPT;
    case 'friendly':
      return FRIENDLY_PROMPT;
    case 'faq':
      return FAQ_PROMPT;
    case 'sales':
      return SALES_PROMPT;
    case 'support':
      return SUPPORT_PROMPT;
    case 'brief':
      return BRIEF_COLLECTION_PROMPT;
    default:
      return CONSULTANT_PROMPT;
  }
}

/**
 * Inject RAG context into prompt
 */
export function injectRAGContext(prompt: string, context: {
  relevantChunks: string;
  fallbackMessage?: string;
}): string {
  return prompt
    .replace('{{RELEVANT_CHUNKS}}', context.relevantChunks)
    .replace('{{FALLBACK_MESSAGE}}', context.fallbackMessage || '');
}

/**
 * Inject proposal data into prompt
 */
export function injectProposalData(prompt: string, data: Record<string, string>): string {
  let result = prompt;
  for (const [key, value] of Object.entries(data)) {
    result = result.replace(new RegExp(`{{${key}}}`, 'g'), value);
  }
  return result;
}

